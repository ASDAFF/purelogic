						<h4>Промышленные станки с ЧПУ</h4>
						<p>Токарный ЧПУ станок - это достаточно обобщенное понятие. С той или иной 					долей точности к промышленным станкам можно отнести до 80% всех 							портальных станков на рынке. Многие деревообрабатывающие компании 							используют несколько таких станков одновременно, для производства 							разноформатной фрезерованной продукции - дверей, табличек, фасадов 							и т.п. Такие станки самые большие по размеру и наиболее дорогие. Однако цена компенсируется долгой наработкой на отказ, надежностью в тяжелых условиях и т.п. Это делает привлекательными промышленные станки в глазах людей, приобретающих станок для извлечения прибыли - простой производства и срыв сроков могут оказаться гораздо более весомыми, чем некоторая переплата за станок. Именно промышленные станки с ЧПУ снабжены максимальным количеством усовершенствований для увеличения производительности и отдачи, таких как ATC(automatic tool changer) - система автоматической смена режущего инструмента шпинделя (фрез, граверов), отдельные стойки ЧПУ с продвинутым управляющим ПО, вакуумные столы, автоматическая система подачи и фильтрации СОЖ, защита направляющих и передач от отходов фрезеровки и пыли, и т.п. На промышленных станках стоят дорогие и зачастую очень мощные шпиндели с малым радиальным биением вала. И, разумеется, спектр обрабатываемых материалов не ограничивается только деревом - все зависит от постановки задач клиентом. К покупке промышленного станка с ЧПУ стоит отнестись максимально ответственно, и как следует проработать характеристики, которые необходимо получить от станка в результате.</p>
