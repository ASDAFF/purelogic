						<p>Фрезерный (рутер) ЧПУ станок - это следующий класс за промышленными
					 	станками. Они стоят на полступени ниже, но это не значит, что точность и 							повторяемость этих станков будут однозначно хуже, чем у 
						индустриальных собратьев. Зачастую они собраны из тех же компонентов
						по тем же самым принципам. Однако, данные станки меньше и легче 
						промышленных, и имеют меньше дополнительного "обвеса" типа ATC. Управляются такие станки, как правило, от персонального компьютера напрямую через LPT, или с применением специализированных контроллеров, подключаемых к компьютеру через USB или Ethernet. Станки данного типа популярны у малого бизнеса, индивидуальных предпринимателей, в хоббийном производстве мебели, моделей, макетов и т.п.: покупка дорогого промышленного станка с ЧПУ не окупится, сделает бизнес неконкурентоспособным, да и нагрузить мощный агрегат зачастую просто не удастся на его полную мощность. По причине существенно меньшего среднего "пробега" за свою рабочую жизнь, портальные фрезерные станки среднего класса реже комплектуются такими вещами, как защита от пыли и стружки, систем автоматической смазки и т.п. Меньшая масса станков также означает некоторое уменьшение спектра материалов, который доступен для обработки на таких станках. Среди них гораздо реже можно найти модели, способные фрезеровать камень и металлы.
